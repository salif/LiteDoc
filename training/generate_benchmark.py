#!/usr/bin/env python3
"""
generate_benchmark.py — one command to produce the release A/B report.

Runs the current tuned params (candidate) against the v2.6.0 hand-set baseline on a
freshly-synthesized held-out set, then writes:
  * BENCHMARK.md            — the full per-category report (drop into the LiteDoc repo)
  * release_blurb.txt       — a short "training improvement" paragraph for RELEASE_NOTES

Run this on the training server (it needs the dataset generator + headless Chromium),
then copy BENCHMARK.md into the public repo for the release. You run it manually; the
report content is generated automatically from real measurements — no hand-editing.

    python training/generate_benchmark.py --version 3.1.0

By default it regenerates the holdout set first (fresh seed each run, so the report
reflects unseen data). Pass --no-resynth to reuse the existing holdout_materials.
"""

import argparse
import json
import os
import random
import subprocess
import sys
from datetime import datetime, timezone

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
REPO_ROOT = os.path.dirname(SCRIPT_DIR)
sys.path.insert(0, SCRIPT_DIR)

from optimizer.fitness import HOLDOUT_DIR, evaluate_parameters

BASELINE_PATH = os.path.join(SCRIPT_DIR, "baseline_v2.6.0.json")
CANDIDATE_PATH = os.path.join(SCRIPT_DIR, "current_params.json")
BENCHMARK_OUT = os.path.join(REPO_ROOT, "BENCHMARK.md")
BLURB_OUT = os.path.join(SCRIPT_DIR, "release_blurb.txt")

CATEGORIES = ["Academic", "Book", "Notebook", "Magazine", "Scanned", "Cursed"]


def load_params(path):
    with open(path, encoding="utf-8") as f:
        data = json.load(f)
    return {k: v for k, v in data.items() if not k.startswith("_")}


def resynthesize_holdout(per_category: int):
    seed = random.randint(1, 10_000_000)
    print(f"Regenerating holdout set (fresh seed={seed}, {per_category}/category)...")
    subprocess.run(
        [sys.executable, "training/orchestrator.py", "--synthesize-holdout", str(per_category),
         "--holdout-seed", str(seed)],
        cwd=REPO_ROOT, check=True,
    )
    return seed


def pct(x):
    return f"{x * 100:.2f}%"


def render_benchmark_md(version, base_score, base_bd, cand_score, cand_bd, seed_note):
    rows = []
    for cat in CATEGORIES:
        b = base_bd.get(cat, {})
        c = cand_bd.get(cat, {})
        if b.get("expected", 0) == 0:
            continue
        bs, cs = b["score"] * 100, c["score"] * 100
        d = cs - bs
        rows.append(f"| {cat:<9} | {bs:16.2f}% | {cs:14.2f}% | {'+' if d >= 0 else ''}{d:.2f} |")
    overall_d = (cand_score - base_score) * 100
    rows.append(f"| **Overall** | **{pct(base_score)}** | **{pct(cand_score)}** | "
                f"**{'+' if overall_d >= 0 else ''}{overall_d:.2f}** |")
    table = "\n".join(rows)

    return f"""# Extraction Benchmark — v{version} tuned parameters vs. v2.6.0 defaults

Auto-generated by `training/generate_benchmark.py` on {datetime.now(timezone.utc).date()}.

LiteDoc's 13 layout/OCR heuristic parameters (`DEFAULT_CONFIG` in the parser) are tuned
by the Bayesian optimizer in [`training/`](training/). This is the A/B comparison of the
current tuned parameters against the previous hand-set defaults.

## Method

- **Baseline:** the hand-set defaults shipped through v2.6.0 (`training/baseline_v2.6.0.json`).
- **Candidate:** the current tuned parameters (`training/current_params.json`).
- **Dataset:** a **held-out** set the optimizer never saw during tuning ({seed_note}).
- **Scoring:** per-document blend of prose, tables, and math (RTL for Cursed). Higher is
  better. See `training/benchmark/scoring.py`.

Reproduce:

```bash
python training/generate_benchmark.py --version {version}
```

## Results

| Category  | v2.6.0 (baseline) | v{version} (tuned) | Δ |
|-----------|------------------:|-------------------:|--------------:|
{table}

Absolute scores reflect a deliberately hard synthetic set (including degraded "Cursed"
pages with RTL Arabic and rasterization), not clean real-world PDFs — treat them as a
*relative* measure of the tuning's effect.
"""


def render_blurb(version, base_score, cand_score, cand_bd, base_bd):
    overall_d = (cand_score - base_score) * 100
    # Find the biggest single-category win for a concrete highlight.
    best_cat, best_gain = None, -999.0
    for cat in CATEGORIES:
        b, c = base_bd.get(cat, {}), cand_bd.get(cat, {})
        if b.get("expected", 0) == 0:
            continue
        gain = (c["score"] - b["score"]) * 100
        if gain > best_gain:
            best_cat, best_gain = cat, gain
    return (
        f"## v{version} — Tuned Extraction Parameters\n\n"
        f"The parser's 13 layout/OCR heuristic parameters are tuned by the open-source "
        f"Bayesian optimizer in `training/`. Measured against a held-out set the optimizer "
        f"never saw during tuning, the new parameters improve overall extraction by "
        f"**{'+' if overall_d >= 0 else ''}{overall_d:.2f} percentage points** over the "
        f"v2.6.0 defaults"
        + (f", with the biggest gain on {best_cat} (**{'+' if best_gain >= 0 else ''}"
           f"{best_gain:.1f} points**)" if best_cat else "")
        + ". Full per-category breakdown and how to reproduce it: [`BENCHMARK.md`](BENCHMARK.md).\n"
    )


def main():
    parser = argparse.ArgumentParser(description="Generate the release A/B benchmark report")
    parser.add_argument("--version", required=True, help="Candidate/release version, e.g. 3.1.0")
    parser.add_argument("--per-category", type=int, default=5, help="Holdout docs per category")
    parser.add_argument("--no-resynth", action="store_true", help="Reuse existing holdout_materials")
    args = parser.parse_args()

    if args.no_resynth:
        seed_note = "the existing training/holdout_materials"
    else:
        seed = resynthesize_holdout(args.per_category)
        seed_note = f"regenerated fresh for this run, seed {seed}"

    baseline = load_params(BASELINE_PATH)
    candidate = load_params(CANDIDATE_PATH)

    print("Evaluating baseline (v2.6.0 defaults)...")
    base_score, base_bd = evaluate_parameters(baseline, HOLDOUT_DIR)
    print("Evaluating candidate (current tuned params)...")
    cand_score, cand_bd = evaluate_parameters(candidate, HOLDOUT_DIR)

    with open(BENCHMARK_OUT, "w", encoding="utf-8") as f:
        f.write(render_benchmark_md(args.version, base_score, base_bd, cand_score, cand_bd, seed_note))
    with open(BLURB_OUT, "w", encoding="utf-8") as f:
        f.write(render_blurb(args.version, base_score, cand_score, cand_bd, base_bd))

    delta = (cand_score - base_score) * 100
    print(f"\nOverall: {pct(base_score)} → {pct(cand_score)}  ({'+' if delta >= 0 else ''}{delta:.2f} pts)")
    print(f"Wrote {BENCHMARK_OUT}")
    print(f"Wrote {BLURB_OUT} (paste into RELEASE_NOTES.md)")


if __name__ == "__main__":
    main()
